package ge.Task;

public class Hired {
    private String proffesion;
    private double salary;

    public Hired(String proffesion, double salary) {
        this.proffesion = proffesion;
        this.salary = salary;
    }

    public String getProffesion() {
        return proffesion;
    }

    public double getSalary() {
        return salary;
    }
}
